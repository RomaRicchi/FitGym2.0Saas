-- Credenciales OAuth de MercadoPago para cada tenant (gimnasio)
-- Cada empresa tiene sus propias credenciales para recibir pagos directamente
CREATE TABLE mercado_pago_credencial (
  id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  tenant_id integer NOT NULL,
  access_token text NOT NULL,
  refresh_token text,
  token_expira_en timestamp with time zone NOT NULL,
  mercado_pago_user_id bigint,
  public_key text,
  activo smallint NOT NULL DEFAULT 1 CHECK (activo IN (0,1)),
  fecha_autorizacion timestamp with time zone NOT NULL DEFAULT CURRENT_TIMESTAMP,
  ultimo_refresco timestamp with time zone,
  created_at timestamp with time zone NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at timestamp with time zone,
  CONSTRAINT uq_mercado_pago_credencial_tenant UNIQUE (tenant_id),
  CONSTRAINT fk_mercado_pago_credencial_tenants FOREIGN KEY (tenant_id) REFERENCES tenants(id) ON DELETE CASCADE
);

-- Sistema de reintentos para webhooks de MercadoPago
-- Asegura que los pagos se procesen correctamente para cada tenant
CREATE TABLE webhook_retry (
  id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  payment_id text NOT NULL,
  tipo text NOT NULL, -- payment/refunded/chargeback
  payload text NOT NULL, -- raw JSON para auditoría
  intentos integer NOT NULL DEFAULT 0,
  next_retry_at timestamp with time zone NOT NULL,
  completado boolean NOT NULL DEFAULT false,
  error_ultimo_intento text,
  created_at timestamp with time zone NOT NULL DEFAULT now(),
  updated_at timestamp with time zone NOT NULL DEFAULT now(),
  tenant_id integer
);

-- Auditoría de pagos de MercadoPago
-- Registra todos los eventos de pagos para trazabilidad
CREATE TABLE payment_audit (
  id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  tenant_id integer,
  type text NOT NULL, -- preference|payment|preapproval|webhook
  reference_id text NOT NULL, -- payment_id, preference_id, preapproval_id
  status text NOT NULL,
  amount numeric,
  currency text,
  email text,
  message text,
  payload text,
  created_at timestamp with time zone NOT NULL DEFAULT now(),
  updated_at timestamp with time zone NOT NULL DEFAULT now()
);

