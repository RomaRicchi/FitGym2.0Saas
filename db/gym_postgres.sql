CREATE TABLE planes_saas (
  id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  nombre varchar(50) NOT NULL,
  descripcion varchar(255),
  precio_mensual numeric(10,2) NOT NULL,
  precio_anual numeric(10,2) NOT NULL,
  max_socios integer NOT NULL DEFAULT 0,  -- 0 = ilimitado
  max_personal integer NOT NULL DEFAULT 0,
  max_salas integer NOT NULL DEFAULT 0,
  max_turnos_por_dia integer NOT NULL DEFAULT 0,
  activo boolean NOT NULL DEFAULT TRUE,
  orden integer NOT NULL DEFAULT 0,
  created_at timestamp without time zone NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at timestamp without time zone
);

CREATE TABLE caracteristicas_plan_saas (
  id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  plan_id integer NOT NULL,
  codigo varchar(50) NOT NULL,
  valor boolean NOT NULL DEFAULT TRUE,
  created_at timestamp without time zone NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at timestamp without time zone,
  FOREIGN KEY (plan_id) REFERENCES planes_saas(id) ON DELETE CASCADE
);

-- ============================================
-- TENANT TABLE (Multi-tenancy)
-- ============================================

CREATE TABLE tenants (
  id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  nombre varchar(160) NOT NULL,
  slug varchar(160),
  logo text,
  email varchar(320),
  telefono varchar(64),
  direccion varchar(255),
  activo smallint NOT NULL DEFAULT 1 CHECK (activo IN (0,1)),

  -- Plan SaaS (FK a planes_saas)
  plan_saas_id integer NOT NULL DEFAULT 1,
  plan_vence_en timestamp without time zone,
  plan_anual smallint NOT NULL DEFAULT 0 CHECK (plan_anual IN (0,1)),

  -- NOTA: El Tenant ID 1 es siempre SuperTenant (hardcoded en PlanSaaSService)
  -- No requiere campo en base de datos.

  -- Límites personalizados (override del plan)
  max_socios_override integer,
  max_personal_override integer,

  -- Branding personalizado (planes Premium y Enterprise)
  primary_color text,              -- Color primario en HEX (ej: '#FF5722')
  logo_url text,                   -- URL del logo personalizado (sobrescribe el campo logo)

  -- Fondos personalizados (planes Premium y Enterprise)
  background_image_url text,       -- URL de imagen de fondo para las vistas
  login_background_image_url text, -- URL de imagen de fondo específica para el login
  login_background_type text,      -- Tipo de fondo para login: 'image' o 'solid'
  login_background_color text,     -- Color de fondo sólido para login (cuando login_background_type = 'solid')

  -- Estadísticas y referidos
  fecha_registro timestamp without time zone NOT NULL DEFAULT CURRENT_TIMESTAMP,
  codigo_referido varchar(50),

  created_at timestamp without time zone NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at timestamp without time zone,

  FOREIGN KEY (plan_saas_id) REFERENCES planes_saas(id)
);

