-- Métodos de pago de los tenants (gimnasios) para el SaaS
CREATE TABLE metodo_pago (
  id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  tenant_id integer NOT NULL,
  tipo varchar(50) NOT NULL, -- mercadopago, stripe, etc.
  token text, -- Token del método de pago (seguro)
  email varchar(320),
  estado integer NOT NULL DEFAULT 1, -- 1=Activo, 2=Suspendido, 3=Expirado, 4=Rechazado
  ultimo_4 char(4), -- Últimos 4 dígitos de la tarjeta
  marca varchar(50), -- Visa, Mastercard, etc.
  expiracion timestamp without time zone, -- Fecha de expiración de la tarjeta
  es_principal smallint NOT NULL DEFAULT 0, -- Método de pago principal
  mercado_pago_customer_id text, -- ID del cliente en MercadoPago
  mercado_pago_card_id text, -- ID de la tarjeta en MercadoPago
  created_at timestamp without time zone NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at timestamp without time zone,
  CONSTRAINT chk_metodo_pago_estado CHECK (estado IN (1, 2, 3, 4)),
  CONSTRAINT chk_metodo_pago_es_principal CHECK (es_principal IN (0, 1)),
  CONSTRAINT fk_metodo_pago_tenant FOREIGN KEY (tenant_id) REFERENCES tenants(id) ON DELETE CASCADE
);

-- Historial de pagos SaaS realizados por los tenants
CREATE TABLE historial_pago_saas (
  id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  tenant_id integer NOT NULL,
  metodo_pago_id integer,
  plan_saas_id integer NOT NULL,
  monto numeric NOT NULL,
  estado integer NOT NULL, -- 1=Pendiente, 2=Exitoso, 3=Fallido, 4=Reembolsado, 5=Chargeback
  moneda varchar(3) DEFAULT 'ARS',
  fecha_intento timestamp without time zone NOT NULL DEFAULT CURRENT_TIMESTAMP,
  fecha_procesamiento timestamp without time zone,
  respuesta_gateway text, -- JSON con la respuesta del gateway
  mensaje_error text,
  transaccion_id text, -- ID de la transacción en el gateway
  intentos_realizados integer NOT NULL DEFAULT 1,
  es_renovacion_automatica smallint NOT NULL DEFAULT 1,
  created_at timestamp without time zone NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at timestamp without time zone,
  CONSTRAINT fk_historial_pagos_tenant FOREIGN KEY (tenant_id) REFERENCES tenants(id) ON DELETE CASCADE,
  CONSTRAINT fk_historial_pagos_metodo_pago FOREIGN KEY (metodo_pago_id) REFERENCES metodo_pago(id) ON DELETE SET NULL,
  CONSTRAINT fk_historial_pagos_plan_saas FOREIGN KEY (plan_saas_id) REFERENCES planes_saas(id)
);

